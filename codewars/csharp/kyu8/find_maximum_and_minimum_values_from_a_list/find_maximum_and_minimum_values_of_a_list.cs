using System.Linq;

namespace Codewars.Solutions.Kyu8.find_maximum_and_minimum_values_from_a_list;

public class FindMaximumAndMinimumValuesOfAList
{
    public static int Min(int[] list)
    {
        return list.Min();
    }

    public static int Max(int[] list)
    {
        return list.Max();
    }
}
